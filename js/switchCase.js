
console.log('****************SwITCH CASE****************');
let colour = 'green';
switch (colour) {
    case 'red':
        console.log('Colour is red');
        break;
    case 'blue':
        console.log('Colour is blue');
        break;
    case 'green':
        console.log('Colour is green');
        break;
    default:
        console.log('Colour is not red, blue or green');
        break;
}
console.log('xxxxxxxxxSWITCH CASExxxxxxxxxxxxx');
